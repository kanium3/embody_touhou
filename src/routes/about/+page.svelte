<h1 class="text-3xl my-3">Embody Touhou</h1>
<p>ブログやプレゼンテーションなどの発表活動を通して、東方Projectの魅力を見つける企画です。</p>
<h2 class="text-2xl my-2.5">詳しく</h2>
<p class="break-all">
    <a href="https://touhou-project.news/touhou_project/" class="underline decoration-2 decoration-aqn">東方Project</a>に関するブログ記事やプレゼンテーション、動画などを作成し、それを発表するという企画です。<br />
    また、さながらアドベントカレンダーのような形式で、毎週日曜日に発表活動を行うことで東方Projectの姿を見出していく狙いもあります。
</p>
<p>ざっくりとした説明は<a href="https://note.com/kanium3/n/n35bbe478eeab" class="underline decoration-2 decoration-aqn">この記事</a>にも載っているので見てみてください。</p>
<h2 class="text-2xl my-2.5">参加したい場合</h2>
<p class="break-all">
    この企画は、東方好きの集うDiscordサーバー<a href="https://discord.gg/gdbqUJ84Wp" class="underline decoration-2 decoration-aqn">「東方談笑所」</a>が運営しております。
    参加したい場合はリンクよりサーバーへの参加お願いします。
</p>
<h2 class="text-2xl my-2.5">このサイトについて</h2>
<p class="break-all">
    このサイトの保守要員は常に人手不足です。PullRequestも受け付けているので、協力して頂ける方はぜひよろしくお願いします。
    </p>
<p class="break-all">
    また、運営の方で手伝ってくれる方も常に募集しています。そちらはDiscordのほうからご一報お願いします。
</p>
<p class="break-all">日々のメンテナンスや機能追加などのリクエストは<a href="https://github.com/kanium3/embody_touhou" class="underline decoration-2 decoration-aqn">GitHub</a>からIssueのほどよろしくお願いします。</p>
